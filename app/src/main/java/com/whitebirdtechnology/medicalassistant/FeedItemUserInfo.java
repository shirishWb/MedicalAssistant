public class FeedItemUserInfo {

    String stringName;
    String stringOccu;
    String stringId;
    String stringImgPath;
    String stringMobileNo;
    String stringIsFav;

    public String getStringName() {
        return stringName;
    }

    public void setStringName(String stringName) {
        this.stringName = stringName;
    }

    public String getStringOccu() {
        return stringOccu;
    }

    public void setStringOccu(String stringOccu) {
        this.stringOccu = stringOccu;
    }

    public String getStringId() {
        return stringId;
    }

    public void setStringId(String stringId) {
        this.stringId = stringId;
    }

    public String getStringImgPath() {
        return stringImgPath;
    }

    public void setStringImgPath(String stringImgPath) {
        this.stringImgPath = stringImgPath;
    }

    public String getStringMobileNo() {
        return stringMobileNo;
    }

    public void setStringMobileNo(String stringMobileNo) {
        this.stringMobileNo = stringMobileNo;
    }

    public String getStringIsFav() {

        return stringIsFav;
    }

    public void setStringIsFav(String stringIsFav) {
        this.stringIsFav = stringIsFav;
    }
}
